    public Success_Fail(int ID, AddNewSemster sm) {
        this.setLocation(200, 100);
        this.setResizable(false);
        this.setTitle("Student Success/Fail Frame"); 
        frame=sm;
        stdID=ID;
        initComponents();
        //loading data in list of student's courses
        try{
            
            query = prc.get_Current_Year();
            r=c.connection(query);
            while(r.next()){
                jLabel2.setText(r.getString(1));
            }
            query=prc.GetNumberofCourseforStudent(stdID);
            r=c.connection(query);
            int index=0;
            while(r.next()){
                index=Integer.parseInt(r.getString(1));
            }
            int[] crsIDs=new int[index];
            query=prc.GetTakensCourseID(ID);
            r=c.connection(query);
            for(int i=0; r.next(); i++){
                crsIDs[i]=Integer.parseInt(r.getString(1));
            }
            DefaultListModel listModel=new DefaultListModel();
            for(int i=0; i<index; i++){
                query=prc.GetCourseName(crsIDs[i]);
                r=c.connection(query);
                while(r.next()){
                    listModel.addElement(r.getString(1));
                }
            }
            jList2.setModel(listModel);
        }
        catch(Exception ex){
          JOptionPane.showMessageDialog(null, "ERROR"+ex.toString());
        }
        jButton45.setVisible(false);
    }
  
    
    

