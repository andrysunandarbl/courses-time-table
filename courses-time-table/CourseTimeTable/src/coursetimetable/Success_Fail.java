    private void jButton45MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton45MouseClicked
        // TODO add your handling code here:
        try{
           // list1.clear();
            list1=(DefaultListModel) jList1.getModel();
            Object[] subjects = list1.toArray();
            int[] IDs=new int[subjects.length];
            for(int i =0; i<subjects.length; i++){
                query=prc.GetCourseID(subjects[i].toString());
                r=c.connection(query);
                while(r.next()){
                    IDs[i]=Integer.parseInt(r.getString(1));
                }
            }
            for(int i=0; i<IDs.length; i++){
            query=prc.Drop_course_from_person(String.valueOf(IDs[i]), String.valueOf(stdID));
            check=c.UpdateConnection(query);
            }
            if(check == 1)
                        JOptionPane.showMessageDialog(null, "Changes Saved Successfully", "Success Operation", JOptionPane.INFORMATION_MESSAGE);
                    else
                        JOptionPane.showMessageDialog(null, "Can't be Saved", "Unsuccess Operation", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception ex){
          JOptionPane.showMessageDialog(null, "ERROR"+ex.toString());
        }
        this.dispose();
        frame.setEnabled(true);
    }//GEN-LAST:event_jButton45MouseClicked

